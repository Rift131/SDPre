public class Objective7Lab2 {

  public static void main(String[] args) {
    // establish a counter at zero
    int counter = 0;
    // perform a while loop that will print the counter and add 1 to the counter so long as it is less than or equal to 20
    while (counter <= 20) {
      System.out.println(counter);
      counter++;
    }
  }
}
