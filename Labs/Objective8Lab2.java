public class Objective8Lab2 {

  public static void main(String[] args) {
    // set a counter variable starting at 0
    int result = 0;
    // iterate and add i to the counter each time through up to 20
    for (int i = 0; i <= 20; i++) {
      result += i;
    }
    System.out.println(result);
  }
}
